#include "libft.h"

int		get_rgb(int r, int g, int b)
{
	int clr;

	if (r > 255 || g > 255 || b > 255 || r < 0 || g < 0 || b < 0)
		return (0);
	clr = (r * 256 * 256) + (g * 256) + b;
	return (clr);
}
